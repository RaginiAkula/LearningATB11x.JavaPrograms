package ex_10_For_Loop;

public class Lab106_For_Loop {
    public static void main(String[] args) {
        for(long i = 1l;i<=10;i++){ // 1 t0 10, 10 times
            System.out.println(i);
        }
        for (float j =0.00f;j<=10.67;j++){ //0 t0 11, 11 times
            System.out.println(j);
        }
    }
}
