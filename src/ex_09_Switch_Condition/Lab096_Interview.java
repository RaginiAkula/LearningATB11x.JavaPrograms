package ex_09_Switch_Condition;

public class Lab096_Interview{
    public static void main(String[] args) {
        char code = 'C';
        switch(code){
            default :
                System.out.println("Helloooooo");
                break;
            case 'A':
                System.out.println("Code is executed");
                break;
            case 'B':
                System.out.println("code is executed ");
        }
    }
}
